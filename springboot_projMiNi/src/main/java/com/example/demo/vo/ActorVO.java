package com.example.demo.vo;

public class ActorVO {

	private int actorID;
	private String actorName;
	private String actorAddr;
	private String actorCategory;
	private int actorFees;
	public int getActorID() {
		return actorID;
	}
	public void setActorID(int actorID) {
		this.actorID = actorID;
	}
	public String getActorName() {
		return actorName;
	}
	public void setActorName(String actorName) {
		this.actorName = actorName;
	}
	public String getActorAddr() {
		return actorAddr;
	}
	public void setActorAddr(String actorAddr) {
		this.actorAddr = actorAddr;
	}
	public String getActorCategory() {
		return actorCategory;
	}
	public void setActorCategory(String actorCategory) {
		this.actorCategory = actorCategory;
	}
	public int getActorFees() {
		return actorFees;
	}
	public void setActorFees(int actorFees) {
		this.actorFees = actorFees;
	}
	
	
}
